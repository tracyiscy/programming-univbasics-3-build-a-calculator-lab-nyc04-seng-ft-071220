def addition(num1, num2)
  addition=num1+num2
  addition
end

def subtraction(num1, num2)
  subtraction=num1-num2
  subtraction
end

def division(num1, num2)
  division=num1/num2
end

def multiplication(num1, num2)
  multiplication=num1*num2
  multiplication
end

def modulo(num1, num2)
  modulo=num1%num2
  modulo
end

def square_root(num)
  square_root=Math.sqrt(num)
  square_root
end
